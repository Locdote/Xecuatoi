package com.xecuatoi.object;

public class Xecuatoi {

	private String id;
	private String trangchu;
	private String mauxe;
	private String giaxe;
	private String tragop;
	private String tintuc;
	private String lienhe;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTrangchu() {
		return trangchu;
	}
	public void setTrangchu(String trangchu) {
		this.trangchu = trangchu;
	}
	public String getMauxe() {
		return mauxe;
	}
	public void setMauxe(String mauxe) {
		this.mauxe = mauxe;
	}
	public String getGiaxe() {
		return giaxe;
	}
	public void setGiaxe(String giaxe) {
		this.giaxe = giaxe;
	}
	public String getTragop() {
		return tragop;
	}
	public void setTragop(String tragop) {
		this.tragop = tragop;
	}
	public String getTintuc() {
		return tintuc;
	}
	public void setTintuc(String tintuc) {
		this.tintuc = tintuc;
	}
	public String getLienhe() {
		return lienhe;
	}
	public void setLienhe(String lienhe) {
		this.lienhe = lienhe;
	}
	public Xecuatoi(String id, String trangchu, String mauxe, String giaxe, String tragop, String tintuc,
			String lienhe) {
		super();
		this.id = id;
		this.trangchu = trangchu;
		this.mauxe = mauxe;
		this.giaxe = giaxe;
		this.tragop = tragop;
		this.tintuc = tintuc;
		this.lienhe = lienhe;
	}


}
